    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
          String ID = lbl_ID.getText();
        String Name = name.getText();
        String Age = age.getText();
        String Bhw = bhw.getText();
        String Baranggay = baranggay.getText();
        String Status = status.getText();
        
       
           if (Name.hashCode() == 0 || Age.hashCode() == 0 || Bhw.hashCode() == 0 || Baranggay.hashCode() == 0 || Status.hashCode() == 0)
    {
        JOptionPane.showMessageDialog(null, "Some Fields are Empty");
    }
        
           else{
       
        PreparedStatement ps;
        String query = " INSERT INTO `patient_information`(`patient_id`, `name`, `age`, `bhw`, `baranggay`, `status`) VALUES (?,?,?,?,?,?)";
        
        try {
            ps = settings_cnx.getConnection().prepareStatement(query);
            
            ps.setString(1, ID);
            ps.setString(2, Name);
            ps.setString(3, Age);
            ps.setString(4, Bhw);
            ps.setString(5, Baranggay);
            ps.setString(6, Status);
           
           
            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null,"Save Successfully");
            }
clearFields();
        } catch (SQLException ex) {
            Logger.getLogger(settings.class.getName()).log(Level.SEVERE, null, " ");
        }

        
           }     
        
    }//GEN-LAST:event_saveActionPerformed

