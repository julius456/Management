    public void PatientID(){
         
    try {
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("select MAX(patient_id)from patient_information");
        rs.next();
        
        rs.getString("MAX(patient_id)");
        
        if(rs.getString("MAX(patient_id)") == null)
        {
            lbl_ID.setText("P-0000001");
        }
        else 
        {
            long id = Long.parseLong(rs.getString("MAX(patient_id)").substring(2,rs.getString("MAX(patient_id)").length()));
            id++;
            lbl_ID.setText("P-" + String.format("%07d", id));
        }
        
        
        
        
    } catch (SQLException ex) {
        Logger.getLogger(add_patients.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }
